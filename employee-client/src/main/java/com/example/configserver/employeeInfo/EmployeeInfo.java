package com.example.configserver.employeeInfo;

public class EmployeeInfo {
	private int eid;
	private String name;
	private String designation;
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignamtion(String designation) {
		this.designation = designation;
	}


}
